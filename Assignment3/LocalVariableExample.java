package Assignment3;

public class LocalVariableExample {
    public static void main(String[] args) {
        // Declaring local variables
        int number = 10;       // Integer variable
        double pi = 3.14;      // Double variable
        String greeting = "Hello, World!";  // String variable
        
        // Printing the values of local variables
        System.out.println("Integer variable: " + number);
        System.out.println("Double variable: " + pi);
        System.out.println("String variable: " + greeting);
    }
}